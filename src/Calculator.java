public class Calculator {

    public int substraction (int a, int b)
    {
        int sub = a  -b ;
        return sub;
    }

    public int sum (int a, int b)
    {
        int sum = a + b ;
        return sum;
    }

    public int multiplication (int a , int b)
    {
        int mul = a * b;
        return mul;
    }

    public double division (double a, double b)
    {
        double div = a/b;
        return  div;
    }
}
